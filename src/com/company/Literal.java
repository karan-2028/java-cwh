package com.company;

public class Literal {
    public static void main(String[] args) {
        byte a=67;//integer literal
        short b=178;
        int c=250;
        float d=9.0f;//float literal
        double e=198.8D;//double literal
        char f='a';//char literal
        boolean g=true;//boolean literal
        long h =20000000000L;// int lit
        String s= "suman";
        System.out.println(s);

    }
}
